package que_1b; // Make sure this matches your package

// Class demonstrating method overloading
public class MathOperations {
    // Method overloading: Multiply two integers
    public int multiply(int a, int b) {
        return a * b;
    }

    // Method overloading: Multiply three integers
    public int multiply(int a, int b, int c) {
        return a * b * c;
    }
}
